/**
 * Get food items as array for search/display
 */
export function getAllFoodItems(): FoodItem[] {
  return Object.values(indianFoods);
}

/**
 * Search foods by name or alias (case-insensitive)
 */
export function searchFoods(query: string): FoodItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  return getAllFoodItems().filter((food) => {
    const nameMatch = food.name.toLowerCase().includes(q);
    const hindiMatch = food.hindi_name?.toLowerCase().includes(q);
    const aliasMatch = food.aliases?.some((a) => a.toLowerCase().includes(q));
    return nameMatch || hindiMatch || aliasMatch;
  });
}

/**
 * Get budget-friendly protein sources (sorted by protein per rupee)
 */
export function getBudgetProteinSources(dietType: string): FoodItem[] {
  const proteinFoods = getAllFoodItems().filter((f) => {
    if (f.protein_g < 6) return false;
    if (dietType === 'vegetarian' && (f.category === 'protein')) return false;
    return true;
  });

  return proteinFoods.sort((a, b) => {
    // Higher protein per calorie = better value
    const ratioA = a.protein_g / a.calories;
    const ratioB = b.protein_g / b.calories;
    return ratioB - ratioA;
  });
}
